  static String t(String key, String lang) {
    final langMap = _translations[lang] ?? _translations['en']!;
    return langMap[key] ?? _translations['en']![key] ?? key;
  }

  static bool isRTL(String lang) {
    return lang == 'ar' || lang == 'ku';
  }

  static String normalizeNumber(String s) {
    // Convert Arabic-Indic digits to Western digits
    return s
        .replaceAll('٠', '0')
        .replaceAll('١', '1')
        .replaceAll('٢', '2')
        .replaceAll('٣', '3')
        .replaceAll('٤', '4')
        .replaceAll('٥', '5')
        .replaceAll('٦', '6')
        .replaceAll('٧', '7')
        .replaceAll('٨', '8')
        .replaceAll('٩', '9')
        // Also handle Persian digits
        .replaceAll('۰', '0')
        .replaceAll('۱', '1')
        .replaceAll('۲', '2')
        .replaceAll('۳', '3')
        .replaceAll('۴', '4')
        .replaceAll('۵', '5')
        .replaceAll('۶', '6')
        .replaceAll('۷', '7')
        .replaceAll('۸', '8')
        .replaceAll('۹', '9');
  }

  static String formatNumber(num value, {int decimalPlaces = 1}) {
    return value.toStringAsFixed(decimalPlaces);
  }

  static String getLanguageDisplayName(String lang) {
    switch (lang) {
      case 'en':
        return 'English';
      case 'ar':
        return 'العربية';
      case 'ku':
        return 'کوردی';
      default:
        return 'English';
    }
  }

  static List<String> getSupportedLanguages() {
    return ['en', 'ar', 'ku'];
  }
